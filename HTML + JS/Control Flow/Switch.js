const month = 2 // Enter any value and recieve the month that corresponds with the value.

switch (month) {
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("february")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("april")
        break;
    case 5:
        console.log("may")
        break;

    default:
        console.log("Default case match")        
        break;
}